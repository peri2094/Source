/*******************************************************************************
* Function Name  : EXTI3_IRQHandler
* Description    : This function handles External interrupt Line 3 request.
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
//--------USER Button : Motor X, Y Pause--------//

void EXTI3_IRQHandler(void)
{
    if(EXTI_GetITStatus(GPIO_EXTI_Line_KEY3) != RESET) {
        GPIO_SetBits(GPIO_MOTORX, GPIO_MOTORX_ENABLE);
        GPIO_SetBits(GPIO_MOTORY, GPIO_MOTORY_ENABLE);
        EXTI_ClearITPendingBit(GPIO_EXTI_Line_KEY3);
    }
}

